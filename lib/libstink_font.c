/* ── Rendering ──────────────────────────────────────────────────────────────
 *
 * stink_font_char: renders one glyph into a stack-local 8×16 ARGB buffer,
 * then sys_blit()s it.  Pixels where the glyph bit is 0 are transparent
 * (0x00000000) so the background shows through.
 */

void stink_font_char(int x, int y, char c, unsigned int rgb)
{
    unsigned int buf[STINK_FONT_W * STINK_FONT_H]; /* 8*16*4 = 512 bytes */
    const unsigned char *g;
    int row, col;

    if (c < 32 || c > 126)
        g = FONT[0]; /* space */
    else
        g = FONT[(unsigned char)c - 32];

    for (row = 0; row < STINK_FONT_H; row++) {
        unsigned char bits = g[row];
        for (col = 0; col < STINK_FONT_W; col++) {
            buf[row * STINK_FONT_W + col] =
                (bits & (0x80u >> col)) ? (0xFF000000u | rgb) : 0x00000000u;
        }
    }
    sys_blit(x, y, STINK_FONT_W, STINK_FONT_H, buf);
}

int stink_font_strn(int x, int y, const char *s, size_t n, unsigned int rgb)
{
    int ox = x;
    size_t i;
    for (i = 0; i < n && s[i]; i++) {
        stink_font_char(x, y, s[i], rgb);
        x += STINK_FONT_W;
    }
    return x - ox;
}

int stink_font_str(int x, int y, const char *s, unsigned int rgb)
{
    if (!s) return 0;
    int ox = x;
    while (*s) {
        stink_font_char(x, y, *s++, rgb);
        x += STINK_FONT_W;
    }
    return x - ox;
}

const unsigned char *stink_font_get_glyph(char c)
{
    if (c < 32 || c > 126)
        return FONT[0];
    return FONT[(unsigned char)c - 32];
}
